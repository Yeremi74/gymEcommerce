export function getProductGallery(product) {
  if (Array.isArray(product.imageUrls) && product.imageUrls.length > 0) {
    return product.imageUrls
  }
  if (
    typeof product.id === "string" &&
    product.id.includes("custom") &&
    product.imageUrl
  ) {
    return [product.imageUrl]
  }
  const pool = galleryPoolsByCategory[getProductCategoryKey(product.id)] || trendingGalleryPool
  const main = product.imageUrl
  const extra = []
  const used = new Set([main])
  const seed = product.id.split("").reduce((acc, ch) => acc + ch.charCodeAt(0), 0)
  for (let k = 0; k < 2; k++) {
    let found = false
    for (let attempt = 0; attempt < pool.length && !found; attempt++) {
      const idx = (seed + k * 5 + attempt) % pool.length
      const url = pool[idx]
      if (!used.has(url)) {
        used.add(url)
        extra.push(url)
        found = true
      }
    }
  }
  while (extra.length < 2) {
    extra.push(main)
  }
  return [main, extra[0], extra[1]]
}

